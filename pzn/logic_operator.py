# operator Logika DAN 
print(True and True)

print(True and False)

print(False and False)

print(False and True)

# jika ada false di kiri atau kanan hasilnya tetap false LOGIKA DAN

# operator logika OR 

print(True or True)

print(True or False)

print(False or True)

print(False or False)

# Jika ada true di kira atau kanan hasilnya tetap true kebalikan dengan dan LOGIKA OR